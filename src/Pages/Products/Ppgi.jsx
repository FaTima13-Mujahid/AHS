<p>Corrugated galvanised iron or steel (colloquially corrugated iron or pailing (in Caribbean English), corrugated sheet metal (in North America), occasionally abbreviated CGI) is a building material composed of sheets of hot-dip galvanised mild steel, cold-rolled to produce a linear corrugated pattern in them. The corrugations increase the bending strength of the sheet in the direction perpendicular to the corrugations, but not parallel to them. Normally each sheet is manufactured longer in its strong direction.</p>

